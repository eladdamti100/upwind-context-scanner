// LogOptions controls log generation and optional secret planting.
type LogOptions struct {
	MinLines int
	// PlantLine, when >= 0, appends a real-shaped secret to the line at that index.
	PlantLine int
	// Plant supplies the secret fragment to append (e.g. "stripe_key="+g.StripeLive()).
	Plant string
	Service string
}

// NginxLog returns an Nginx access log of >= opts.MinLines lines (in-memory).
func NginxLog(g *secrets.Gen, opts LogOptions) string {
	return toString(func(w io.Writer) error { return StreamNginxLog(w, g, opts) })
}

// StreamNginxLog writes an Nginx access log of >= Scaled(opts.MinLines) lines to
// w. Every line carries a 16-digit txn id (card-shaped FP). When opts.PlantLine
// >= 0 the opts.Plant fragment is buried on that line. PlantLine is interpreted
// against the UNSCALED index space so a plant at line N lands at N regardless of
// Scale.
func StreamNginxLog(w io.Writer, g *secrets.Gen, opts LogOptions) error {
	methods := []string{"GET", "POST", "PUT", "DELETE", "PATCH"}
	routes := []string{"/v2/bookings", "/v2/properties", "/v2/payments", "/assets/app.js", "/healthz", "/metrics", "/v2/quotes", "/api/kyc"}
	agents := []string{"Mozilla/5.0 (X11; Linux x86_64)", "okhttp/4.12", "curl/8.4.0", "Go-http-client/2.0", "python-requests/2.31"}
	codes := []string{"200", "201", "204", "301", "400", "401", "404", "429", "500", "502"}
	lines := Scaled(opts.MinLines)
	for i := 0; i < lines; i++ {
		fmt.Fprintf(w, "%s - - [%02d/Jun/2026:%02d:%02d:%02d +0000] \"%s %s HTTP/1.1\" %s %d txn_id=%s req_id=%s \"%s\"",
			g.IPv4(), 1+(i%28), i%24, i%60, (i*7)%60,
			g.Pick(methods), g.Pick(routes), g.Pick(codes), g.Intn(9000)+100,
			g.TxnID(), g.UUID(), g.Pick(agents))
		if i == opts.PlantLine && opts.Plant != "" {
			io.WriteString(w, " note="+opts.Plant)
		}
		io.WriteString(w, "\n")
	}
	return nil
}

