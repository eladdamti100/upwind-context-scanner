// Package content holds the bulk "haystack" factories shared by every customer
// agent: large secret-free source files in six languages, dependency lockfiles,
// proxy/access logs, and assorted infra docs. Each factory loops until it meets
// a caller-supplied minimum line count so the corpus reaches enterprise scale
// (500+ line code files, 1000+ line lockfiles, 5000+ line logs).
//
// Clean code is deliberately seeded with decoy identifiers (secret_token = "",
// client_secret, apiKey) bound to empty strings or env lookups so shape-only
// scanners over-report — these are the structural false positives.
package content

import (
	"encoding/base64"
	"fmt"
	"io"
	"strings"

	"upwind-context-scanner/pkg/secrets"
)

// Scale multiplies the line/row count of every BULK producer (logs, lockfiles,
// data dumps) so the whole corpus can be dialed from ~15MB up to 100MB+ with a
// single knob, set from the CORPUS_SCALE env var by cmd/generate. Per-file
// source code is intentionally NOT scaled — code files stay realistically sized;
// only the haystack noise grows. Scale<=1 leaves counts untouched.
var Scale = 1

// Scaled returns n multiplied by the active corpus Scale (floored at n).
func Scaled(n int) int {
	if Scale <= 1 {
		return n
	}
	return n * Scale
}

// build runs body() appending lines until at least minLines are present, then
// returns the joined document. Used for non-scaled source-code factories.
func build(head []string, minLines int, body func(i int) []string) string {
	b := append([]string{}, head...)
	for i := 0; len(b) < minLines; i++ {
		b = append(b, body(i)...)
	}
	return strings.Join(b, "\n") + "\n"
}

