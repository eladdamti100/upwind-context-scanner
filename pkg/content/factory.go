// CleanGo returns secret-free Go HTTP-handler boilerplate of >= minLines lines.
func CleanGo(g *secrets.Gen, minLines int) string {
	head := []string{
		"// Code generated by service-scaffold. DO NOT EDIT.",
		"package controllers",
		"",
		"import (",
		"\t\"context\"", "\t\"encoding/json\"", "\t\"fmt\"", "\t\"net/http\"", "\t\"os\"", "\t\"time\"",
		")",
		"",
		"// Client carries decoy fields that shape-only scanners over-flag.",
		"type Client struct {",
		"\taccessToken  string // populated at runtime from the secrets manager",
		"\tclientSecret string",
		"\tapiKey       string",
		"}",
		"",
		"func newClient() *Client {",
		"\taccessToken := \"\"                          // intentionally empty",
		"\tclientSecret := os.Getenv(\"CLIENT_SECRET\") // resolved by orchestrator",
		"\treturn &Client{accessToken: accessToken, clientSecret: clientSecret}",
		"}",
		"",
	}
	return build(head, minLines, func(i int) []string {
		name := fmt.Sprintf("%s%s%d", verbs[i%len(verbs)], ents[(i/len(verbs))%len(ents)], i)
		return []string{
			fmt.Sprintf("// %s handles an incoming request and returns an error on failure.", name),
			fmt.Sprintf("func %s(ctx context.Context, w http.ResponseWriter, r *http.Request) error {", name),
			"\tstart := time.Now()",
			"\tvar payload map[string]interface{}",
			"\tif err := json.NewDecoder(r.Body).Decode(&payload); err != nil {",
			"\t\treturn fmt.Errorf(\"decode body: %w\", err)",
			"\t}",
			fmt.Sprintf("\tpayload[\"_handler\"] = %q", name),
			"\tw.Header().Set(\"Content-Type\", \"application/json\")",
			"\tw.WriteHeader(http.StatusOK)",
			"\t_ = json.NewEncoder(w).Encode(map[string]any{\"ok\": true, \"ms\": time.Since(start).Milliseconds()})",
			"\treturn nil",
			"}",
			"",
		}
	})
}

