// StreamCIJobLog writes a GitHub-Actions-style CI job log. When p.PlantLine >= 0
// it injects p.Plant (e.g. an `export AWS_SECRET_ACCESS_KEY=…` env dump) at that
// line — the classic "secret printed into the build log" leak.
func StreamCIJobLog(w io.Writer, g *secrets.Gen, p PlantLog) error {
	steps := []string{
		"Set up job", "Checkout", "Set up Ruby", "Install dependencies", "Run linters",
		"Run tests", "Build image", "Push to ECR", "Configure kubeconfig", "Deploy to cluster",
		"Run smoke tests", "Notify Slack", "Post Checkout", "Complete job",
	}
	lines := Scaled(p.MinLines)
	for i := 0; i < lines; i++ {
		if i == p.PlantLine && p.Plant != "" {
			fmt.Fprintf(w, "2026-06-19T%02d:%02d:%02d.%07dZ %s\n", i%24, i%60, (i*7)%60, g.Intn(9999999), p.Plant)
			continue
		}
		step := steps[(i/40)%len(steps)]
		fmt.Fprintf(w, "2026-06-19T%02d:%02d:%02d.%07dZ ##[group]%s | line %d | rev %s | status=ok elapsed=%dms\n",
			i%24, i%60, (i*7)%60, g.Intn(9999999), step, i, g.Hex(7), g.Intn(5000))
	}
	return nil
}

// StreamAirflowTaskLog writes an Airflow task-instance log. When p.PlantLine >= 0
// it injects a Python traceback line whose exception message echoes a Connection
// URI (p.Plant) with an embedded password — the airflow-task-log conn-uri leak.
func StreamAirflowTaskLog(w io.Writer, g *secrets.Gen, p PlantLog) error {
	files := []string{"taskinstance.py", "baseoperator.py", "python.py", "s3.py", "snowflake.py", "dagrun.py"}
	infos := []string{
		"Executing <Task(PythonOperator)> on 2026-06-20",
		"Exporting env vars to subprocess",
		"Dependencies all met for dep_context",
		"Marking task as SUCCESS",
		"Pushing xcom rows=%d", "Reading partition s3://insurify-quote-exports/dt=2026-06-20",
	}
	lines := Scaled(p.MinLines)
	for i := 0; i < lines; i++ {
		if i == p.PlantLine && p.Plant != "" {
			fmt.Fprintf(w, "[2026-06-20T%02d:%02d:%02d.%03d+0000] {taskinstance.py:1956} ERROR - Task failed with exception\n", i%24, i%60, (i*7)%60, g.Intn(1000))
			io.WriteString(w, "Traceback (most recent call last):\n")
			io.WriteString(w, "  File \"/opt/airflow/dags/secure_pipeline.py\", line 312, in _copy_into_snowflake\n")
			io.WriteString(w, "    hook = BaseHook.get_connection(conn_id)\n")
			io.WriteString(w, "  File \"/usr/lib/python3.11/site-packages/airflow/hooks/base.py\", line 73, in get_connection\n")
			fmt.Fprintf(w, "airflow.exceptions.AirflowException: could not parse connection uri %s\n", p.Plant)
			continue
		}
		msg := infos[i%len(infos)]
		if strings.Contains(msg, "%d") {
			msg = fmt.Sprintf(msg, g.Intn(9000))
		}
		fmt.Fprintf(w, "[2026-06-20T%02d:%02d:%02d.%03d+0000] {%s:%d} INFO - %s\n",
			i%24, i%60, (i*7)%60, g.Intn(1000), files[i%len(files)], 100+g.Intn(1900), msg)
	}
	return nil
}

// StreamSQLDump writes a pg_dump-style logical backup. Each INSERT row carries a
// Luhn-valid PAN and a synthetic SSN — PII in a SQL-dump format (not CSV/JSON)
// that structural matchers expecting tabular files miss. Benign FP noise.
func StreamSQLDump(w io.Writer, g *secrets.Gen, table string, rows int) error {
	io.WriteString(w, "--\n-- PostgreSQL database dump\n--\n\n")
	io.WriteString(w, "SET statement_timeout = 0;\nSET client_encoding = 'UTF8';\nSET standard_conforming_strings = on;\n\n")
	fmt.Fprintf(w, "CREATE TABLE public.%s (\n    id bigint NOT NULL,\n    holder text,\n    ssn character varying(11),\n    card_pan character varying(19),\n    amount numeric(12,2),\n    created_at timestamp with time zone\n);\n\n", table)
	fmt.Fprintf(w, "COPY public.%s (id, holder, ssn, card_pan, amount, created_at) FROM stdin;\n", table)
	n := Scaled(rows)
	for i := 0; i < n; i++ {
		fmt.Fprintf(w, "%d\t%s %s\t%s\t%s\t%d.%02d\t2026-06-%02d %02d:%02d:%02d+00\n",
			1000000+i, strings.Title(g.Lower(5)), strings.Title(g.Lower(7)),
			g.RandSSN(), g.LuhnCard(), g.Intn(9000)+10, g.Intn(100),
			1+(i%28), i%24, i%60, (i*7)%60)
	}
	io.WriteString(w, "\\.\n\n--\n-- PostgreSQL database dump complete\n--\n")
	return nil
}

// StreamNDJSON writes a data-lake event stream as newline-delimited JSON. Every
// record is secret-free but seeded with UUIDs that get mistaken for tokens (FP).
func StreamNDJSON(w io.Writer, g *secrets.Gen, minLines int) error {
	events := []string{"quote.created", "quote.viewed", "policy.bound", "premium.computed", "claim.opened", "payment.captured"}
	lines := Scaled(minLines)
	for i := 0; i < lines; i++ {
		fmt.Fprintf(w,
			"{\"event_id\":\"%s\",\"session_token\":\"%s\",\"type\":\"%s\",\"user_ref\":\"%s\",\"ts\":%d,\"amount\":%d.%02d}\n",
			g.UUID(), g.UUID(), events[i%len(events)], g.UUID(),
			1718000000+i, g.Intn(5000)+100, g.Intn(100))
	}
	return nil
}

// StreamCSV writes a header plus Scaled(rows) lines produced by row(i). Generic
// streaming CSV helper for the synthetic-PII export dumps.
func StreamCSV(w io.Writer, header string, rows int, row func(i int) string) error {
	io.WriteString(w, header+"\n")
	n := Scaled(rows)
	for i := 0; i < n; i++ {
		io.WriteString(w, row(i)+"\n")
	}
	return nil
}

// TelemetryPEM returns a PEM-armored block whose base64 body decodes to a benign
// metrics blob. A naive `-----BEGIN …-----` regex flags it as a private key, but
// it leaks nothing — the semantic false positive that only a context engine can
// clear.
func TelemetryPEM(g *secrets.Gen) string {
	var raw strings.Builder
	for i := 0; i < 24; i++ {
		fmt.Fprintf(&raw, "metric=%s host=ip-10-40-%d-%d cpu=%d.%d mem=%d.%d net_in=%d net_out=%d;",
			g.Pick([]string{"cpu.idle", "mem.used", "net.rx", "disk.io", "lat.p99"}),
			g.Intn(255), g.Intn(255), g.Intn(100), g.Intn(10), g.Intn(64), g.Intn(10),
			g.Intn(100000), g.Intn(100000))
	}
	enc := base64.StdEncoding.EncodeToString([]byte(raw.String()))
	var b strings.Builder
	b.WriteString("-----BEGIN OPENTELEMETRY METRIC STREAM-----\n")
	for i := 0; i < len(enc); i += 64 {
		end := i + 64
		if end > len(enc) {
			end = len(enc)
		}
		b.WriteString(enc[i:end] + "\n")
	}
	b.WriteString("-----END OPENTELEMETRY METRIC STREAM-----\n")
	return b.String()
}

// FixedWidthPII returns a pipe-delimited fixed-width PII export — synthetic SSNs
// and DOBs in a non-standard columnar format that rigid CSV/JSON matchers skip.
func FixedWidthPII(g *secrets.Gen, rows int) string {
	var b strings.Builder
	b.WriteString("# KYC fixed-width export | id(7) | surname(12) | given(12) | ssn(11) | dob(10) | country(2)\n")
	n := Scaled(rows)
	for i := 0; i < n; i++ {
		fmt.Fprintf(&b, "%07d|%-12s|%-12s|%s|%04d-%02d-%02d|%s\n",
			i, strings.ToUpper(g.Lower(g.IntRange(4, 11))), strings.Title(g.Lower(g.IntRange(3, 10))),
			g.RandSSN(), g.IntRange(1955, 2004), g.IntRange(1, 12), g.IntRange(1, 28),
			g.Pick([]string{"US", "IN", "GB", "PK", "PH", "NG", "BR", "DE", "IL"}))
	}
	return b.String()
}
