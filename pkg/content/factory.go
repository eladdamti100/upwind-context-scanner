// toString drains a streaming producer into a string for the in-memory callers.
func toString(stream func(io.Writer) error) string {
	var sb strings.Builder
	_ = stream(&sb)
	return sb.String()
}

// PlantLog optionally buries a secret fragment on a specific (unscaled) line.
type PlantLog struct {
	MinLines  int
	PlantLine int    // < 0 to plant nothing
	Plant     string // the line content injected at PlantLine
}

// StreamAuditLog writes a CloudWatch/EKS-style audit log as JSON lines. Every
// line is a secret-free Kubernetes audit event — high volume, structurally
// busy, and full of UUIDs/ARNs that keep entropy scanners chattering.
func StreamAuditLog(w io.Writer, g *secrets.Gen, minLines int) error {
	verbs := []string{"get", "list", "watch", "create", "update", "patch", "delete"}
	resources := []string{"pods", "services", "deployments", "secrets", "configmaps", "nodes", "events", "ingresses"}
	namespaces := []string{"auth", "billing", "routing", "observability", "kube-system", "default"}
	users := []string{"system:serviceaccount:kube-system:deployment-controller", "arn:aws:iam::123456789012:role/eks-admin", "system:node:ip-10-40-12-8", "kubelet"}
	lines := Scaled(minLines)
	for i := 0; i < lines; i++ {
		fmt.Fprintf(w,
			"{\"kind\":\"Event\",\"apiVersion\":\"audit.k8s.io/v1\",\"level\":\"RequestResponse\",\"auditID\":\"%s\",\"stage\":\"ResponseComplete\",\"requestURI\":\"/api/v1/namespaces/%s/%s\",\"verb\":\"%s\",\"user\":{\"username\":\"%s\"},\"sourceIPs\":[\"%s\"],\"responseStatus\":{\"code\":%s},\"requestReceivedTimestamp\":\"2026-06-%02dT%02d:%02d:%02d.%03dZ\"}\n",
			g.UUID(), g.Pick(namespaces), g.Pick(resources), g.Pick(verbs), g.Pick(users), g.IPv4(),
			g.Pick([]string{"200", "201", "403", "404", "409"}),
			1+(i%28), i%24, i%60, (i*7)%60, g.Intn(1000))
	}
	return nil
}

